public class ExamReview {
    public static void main(String [] args){
        String holiday = "Thanksgiving";

        switch(holiday){
            case "Easter":
                System.out.println("Happy Easter!");
                break;
            case "Fourth of July":
                System.out.println("Happy 4th of July!");
                break;
            case "Memorial Day":
                System.out.println("Happy Memorial Day!");
                break;
            case "Christmas":
                System.out.println("Merry Christmas!");
                break;
            case "Thanksgiving":
                System.out.println("Happy Thanksgiving!");
                break;
            default:
                System.out.println("Have a great holiday!");
                break;
        }

    }
}
